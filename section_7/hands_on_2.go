//Use following operators, write expressions and assign their values to variables:
// ==
// <=
// >=
// !=
// <
// >

package main

import "fmt"

func main() {
	g := (4 == 4)
	h := (28 <= 3)
	i := (28 >= 3)
	j := ('a' != 'a')
	k := (1 < 1)
	l := (4 > 3)

	fmt.Println(g)
	fmt.Println(h)
	fmt.Println(i)
	fmt.Println(j)
	fmt.Println(k)
	fmt.Println(l)
}
