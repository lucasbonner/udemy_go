//using the short declaration operator assign these values to variables
//with identifiers 'x', 'y', and 'z'
//42, "James Bond", true

//Now print the values stored in variables using
//single print statement and multiple print statements

package main

import "fmt"

func main() {
	x := 42
	y := "James Bond"
	z := true

	fmt.Println(x, y, z)

	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
